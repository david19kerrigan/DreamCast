public class Tile {

    private String type;
    private String image;

    public Tile() {
	type = "grass";
	image = "x";
    }

    public String getType() {
	return type;
    }

    public void setType(String data) {
	type = data;
    }

    public String getImage() {
	return image;
    }

    public void setImage(String data) {
	image = data;
    }

}
